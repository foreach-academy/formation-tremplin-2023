function sayHi(name) {
  return `Bonjour ${name} !`;
}

console.log(sayHi('John'));

function add(a, b) {
  return a + b;
}

console.log(add(5, 7));

function multiply(a, b) {
  return a * b;
}

console.log(multiply(5, 7));

function greet(name, age) {
  return `Bonjour ${name}, vous avez ${age} ans.`;
}

console.log(greet('John', 30));

function square(number) {
  return number ** 2;
}

console.log(square(5));
